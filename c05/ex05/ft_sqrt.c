int	ft_sqrt(int nb)
{	
	unsigned int	n;
	unsigned int	i;
	unsigned int	r;

	n = nb;
	i = 0;
	r = 0;
	if (nb <= 0)
		return (0);
	while (r < n && i < 46340)
	{
		i++;
		r = i * i;
	}
	if (r == n)
		return (i);
	else
		return (0);
}
