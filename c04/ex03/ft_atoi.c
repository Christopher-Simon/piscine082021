int	ft_atoi(char *str)
{
	int	i;
	int	d;
	int	a;

	d = 0;
	i = 0;
	a = 0;
	while (*str == '\t' || *str == '\n' || *str == '\r'
		|| *str == '\v' || *str == ' ' || *str == '\f')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			i++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
		a = 10 * a + (*(str++) - '0');
	if (i % 2 != 0)
		a *= -1;
	return (a);
}
